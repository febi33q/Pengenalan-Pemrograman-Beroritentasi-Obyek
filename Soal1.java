public class Soal1 {
    public static void main(String[] args){
        byte angka1 = 125; // inisialisasi nilai byte angka1
        byte angka2= 6; // inisialisasi nilai byte angka2
        byte hasil = (byte) (angka1+angka2); // casting hasil penjumlahan angka1 dan angka2 ke tipe data byte
        System.out.println("Hasil 1" +hasil); // mencetak hasil dari penjumlahan byte 1 dan byte 2
    }
}